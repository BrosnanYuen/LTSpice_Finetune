#!/usr/bin/env python3
"""Build an Unsloth-ready JSON dataset from LTspice circuit files.

For every basename in ./Circuits-LTSpice that has all 3 files:
- <name>.txt
- <name>.asc
- <name>.net

A dataset entry is created with:
- question: LTSpice_ASC.md + LTSPICE_NET.md + design instruction + Summary from .txt
- answer: full .net + full .asc + full .txt
"""

from __future__ import annotations

import argparse
import json
import re
from pathlib import Path
from typing import Dict, List, Tuple


PROJECT_ROOT = Path(__file__).resolve().parent
DEFAULT_CIRCUITS_ROOT = PROJECT_ROOT / "Circuits-LTSpice"
DEFAULT_ASC_GUIDE = PROJECT_ROOT / "LTSpice_ASC.md"
DEFAULT_NET_GUIDE_CANDIDATES = (
    PROJECT_ROOT / "LTSpice_NET.md",
    PROJECT_ROOT / "LTSPICE_NET.md",
)
DEFAULT_OUTPUT = PROJECT_ROOT / "ltspice_unsloth_dataset.json"

INSTRUCTION2 = (
    "You are an electrical engineering AI assistant specializing in LTSpice circuit design."
    "Design and create .net LTSpice circuit files using the following context:\n\n"
)

INSTRUCTION3 = (
    "You are an electrical engineering AI assistant specializing in LTSpice circuit design."
    "Design and create .asc LTSpice circuit files from .net NETLIST LTSpice circuit file and also using the following context:\n\n"
)

# Remove only known-noisy LTspice comment lines.
# Keep functional lines like `.lib C:\users\...\standard.bjt`.
NOISY_COMMENT_PATTERNS = [
    re.compile(r"^\s*[;*#/]\s*Generated by LTspice .*", re.IGNORECASE),
    re.compile(
        r"^\s*[;*#/]\s*[A-Za-z]:\\.*Circuits-LTSpice.*\.(asc|net|txt)\s*$",
        re.IGNORECASE,
    ),
]


def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8", errors="replace")


def strip_noisy_comments(content: str) -> str:
    kept_lines: List[str] = []
    for line in content.splitlines():
        if any(pattern.match(line) for pattern in NOISY_COMMENT_PATTERNS):
            continue
        kept_lines.append(line)
    return "\n".join(kept_lines).strip() + "\n"


def extract_summary(txt_content: str) -> str:
    # Prefer section explicitly under "Summary:" and before "Simulation:".
    match = re.search(
        r"Summary\s*:\s*(.*?)\n\s*Calculations\s*:",
        txt_content,
        flags=re.IGNORECASE | re.DOTALL,
    )
    if match:
        return match.group(1).strip()

    # Fallback: first non-empty paragraph after "Summary:".
    match = re.search(r"Summary\s*:\s*(.*)", txt_content, flags=re.IGNORECASE | re.DOTALL)
    if match:
        after = match.group(1).strip()
        first_para = re.split(r"\n\s*\n", after, maxsplit=1)[0].strip()
        if first_para:
            return first_para

    # Final fallback: first non-empty line in txt.
    for line in txt_content.splitlines():
        if line.strip():
            return line.strip()
    return "No summary provided."


def extract_calculations(txt_content: str) -> str:
    # Prefer section explicitly under "Summary:" and before "Simulation:".
    match = re.search(
        r"Calculations\s*:\s*(.*?)\n\s*Explanation of .net file\s*:",
        txt_content,
        flags=re.IGNORECASE | re.DOTALL,
    )
    if match:
        return match.group(1).strip()

    return ""



def extract_netlist(txt_content: str) -> str:
    # Prefer section explicitly under "Summary:" and before "Simulation:".
    match = re.search(
        r"Explanation of .net file\s*:\s*(.*?)\n\s*Explanation of .asc file\s*:",
        txt_content,
        flags=re.IGNORECASE | re.DOTALL,
    )
    if match:
        return match.group(1).strip()

    return ""


def extract_ascfile(txt_content: str) -> str:
    # Matches the header and captures everything following it until the end of the file
    match = re.search(
        r"Explanation of .asc file\s*:\s*(.*)", 
        txt_content, 
        flags=re.IGNORECASE | re.DOTALL
    )
    
    if match:
        return match.group(1).strip()

    return ""


def find_matching_triplets(circuits_root: Path) -> List[Tuple[Path, Path, Path]]:
    by_base: Dict[Path, Dict[str, Path]] = {}

    for path in circuits_root.rglob("*"):
        if not path.is_file():
            continue
        ext = path.suffix.lower()
        if ext not in {".txt", ".asc", ".net"}:
            continue
        base = path.with_suffix("")
        by_base.setdefault(base, {})[ext] = path

    triplets: List[Tuple[Path, Path, Path]] = []
    for _, group in by_base.items():
        if {".txt", ".asc", ".net"}.issubset(group):
            triplets.append((group[".txt"], group[".asc"], group[".net"]))

    triplets.sort(key=lambda t: str(t[0]).lower())
    return triplets


def resolve_net_guide() -> Path:
    for candidate in DEFAULT_NET_GUIDE_CANDIDATES:
        if candidate.exists():
            return candidate
    joined = " or ".join(str(p) for p in DEFAULT_NET_GUIDE_CANDIDATES)
    raise FileNotFoundError(f"Could not find net guide file: {joined}")


def build_dataset(circuits_root: Path) -> List[Dict[str, str]]:
    asc_guide = read_text(DEFAULT_ASC_GUIDE).strip()
    net_guide = read_text(resolve_net_guide()).strip()

    triplets = find_matching_triplets(circuits_root)
    dataset: List[Dict[str, str]] = []


    dataset.append(
        {
            "id": "How to create a LTSpice .asc netlist file",
            "question": "How to create a LTSpice .asc netlist file?",
            "answer": asc_guide,
        }
    )

    dataset.append(
        {
            "id": "How to create a LTSpice .net netlist file",
            "question": "How to create a LTSpice .net netlist file?",
            "answer": net_guide,
        }
    )

    firstone = 1

    for txt_path, asc_path, net_path in triplets:
        txt_raw = read_text(txt_path)
        asc_raw = read_text(asc_path)
        net_raw = read_text(net_path)

        summary = extract_summary(txt_raw)
        asc_clean = strip_noisy_comments(asc_raw)
        net_clean = strip_noisy_comments(net_raw)

        # calculations
        calculations = extract_calculations(txt_raw)
        if (len(calculations) == 0):
            print(txt_path)
            continue

        # .net NETLIST DESIGN
        net_explanation = extract_netlist(txt_raw)
        if (len(net_explanation) == 0):
            print(txt_path)
            continue

        # .asc design
        ascfile_explanation = extract_ascfile(txt_raw)
        if (len(ascfile_explanation) == 0):
            print(txt_path)
            continue

        # FULL DESIGN
        question = (
            "You are an electrical engineering AI assistant specializing in LTSpice circuit design.\n"
            f"DESIGN ACCORDING TO THESE SPECIFICATIONS:\n{summary}\n\n"
            "Do the following steps for LTSpice circuit design:\n"
            "Step 1) Calculate all values for the circuit design and give explanations in \"Calculations:\" Section.\n"
            "Step 2) Use the calculations in step 1 to create and design a .net NETLIST file for LTspice and explain line by line in \"Explain .net:\" Section\n"
            "Step 3) Use the .net netlist in step 2 to create and design a .asc file for LTspice and explain line by line in \"Explain .asc:\" Section\n"
            "Step 4) Put the full .net LTSpice file without comments in \"Final LTSpice .net file:\" Section with ```text ```\n"
            "Step 5) Put the full .asc LTSpice file without comments in \"Final LTSpice .asc file:\" Section with ```text ```\n"
        )

        answer = (
            f"Calculations:\n{calculations}\n\n"
            f"Explain .net:\n{net_explanation}\n\n"
            f"Explain .asc:\n{ascfile_explanation}\n\n"
            f"Final LTSpice .net file:\n"
            f"```text\n{net_clean}```\n\n"
            f"Final LTSpice .asc file:\n"
            f"```text\n{asc_clean}```\n\n"
        )

        dataset.append(
            {
                "id": str(txt_path.relative_to(circuits_root).with_suffix("")),
                "question": question,
                "answer": answer,
            }
        )










        # Calculations only
        question = (
            "You are an electrical engineering AI assistant specializing in LTSpice circuit design.\n"
            f"DESIGN ACCORDING TO THESE SPECIFICATIONS:\n{summary}\n\n"
            "Do the following steps for LTSpice circuit design:\n"
            "Step 1) Calculate all values for the circuit design and give explanations in \"Calculations:\" Section.\n"
        )

        answer = (
            f"Calculations:\n{calculations}\n\n"
        )

        dataset.append(
            {
                "id": str(txt_path.relative_to(circuits_root).with_suffix("")),
                "question": question,
                "answer": answer,
            }
        )








        # .net DESIGN
        question = (
            "You are an electrical engineering AI assistant specializing in LTSpice circuit design.\n"
            f"DESIGN ACCORDING TO THESE SPECIFICATIONS:\n{summary}\n\n"
            "Do the following steps for LTSpice circuit design:\n"
            "Step 1) Calculate all values for the circuit design and give explanations in \"Calculations:\" Section.\n"
            "Step 2) Use the calculations in step 1 to create and design a .net NETLIST file for LTspice and explain line by line in \"Explain .net:\" Section\n"
            "Step 3) Put the full .net LTSpice file without comments in \"Final LTSpice .net file:\" Section with ```text ```\n"
        )

        answer = (
            f"Calculations:\n{calculations}\n\n"
            f"Explain .net:\n{net_explanation}\n\n"
            f"Final LTSpice .net file:\n"
            f"```text\n{net_clean}```\n\n"
        )

        if (firstone == 1):
            with open("input.txt", "w") as f:
                f.write(question)
            with open("output.txt", "w") as f:
                f.write(answer)
            firstone = 0

        dataset.append(
            {
                "id": str(txt_path.relative_to(circuits_root).with_suffix("")),
                "question": question,
                "answer": answer,
            }
        )





    return dataset


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Create LTspice Q/A JSON dataset for Unsloth/Qwen3.5 training."
    )
    parser.add_argument(
        "--circuits-root",
        type=Path,
        default=DEFAULT_CIRCUITS_ROOT,
        help="Root folder to scan for .txt/.asc/.net files.",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=DEFAULT_OUTPUT,
        help="Output JSON file path.",
    )
    args = parser.parse_args()

    dataset = build_dataset(args.circuits_root)
    args.output.write_text(
        json.dumps(dataset, indent=2, ensure_ascii=False),
        encoding="utf-8",
    )

    print(f"Wrote {len(dataset)} entries to {args.output}")


if __name__ == "__main__":
    main()
